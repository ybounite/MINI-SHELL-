#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	nb2;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		nb2 = n % 10 + 48;
		write(fd, &nb2, 1);
	}
}
